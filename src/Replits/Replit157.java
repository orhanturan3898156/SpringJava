public class Replit157 {
    public static void main(String[] args) {
        int a = 1;
        int b = 10;
        int result = max(a, b);
        System.out.println(result);

    }

    public static int max(int x, int y) {
        if (x > y) {
            return y;
        } else {
            return x;
        }


    }
}
