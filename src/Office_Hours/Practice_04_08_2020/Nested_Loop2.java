package Office_Hours.Practice_04_08_2020;

public class Nested_Loop2 {
    public static void main(String[] args) {


        for (int j=1;j<=6;j++){
            for (int i=1;i<=j;i++){
                System.out.print("* ");
            }
            System.out.println();

        }

        //System.out.println("\n===========================\n");


        for (int b=7;b>=1;b--){
            for (int a=1;a<=b;a++){
                System.out.print("* ");
            }
            System.out.println();


        }









    }
}
