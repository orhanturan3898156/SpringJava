package Day18_Nested_Loop_DoWhile;

public class Do_While_Loop2 {
    public static void main(String[] args) {

        int number=103;

        while (number>=100){
            System.out.println(number);
            number--;
        }

        System.out.println("====================================\n");

        int number2=10;

        do {
            System.out.println(number2);
            number2--;
        }while (number2>=100);








    }
}
