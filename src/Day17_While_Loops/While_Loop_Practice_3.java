package Day17_While_Loops;

public class While_Loop_Practice_3 {
    public static void main(String[] args) {


        for (int i=0;i<100;i++){
            System.out.print("*");
        }

        int a=0;
        while(a<100){
            System.out.print("*");
            a++;
        }

        System.out.println();

        int number=3;
        System.out.println("***");

        while (number>0){
            System.out.println("* *");
            number--;
        }
        System.out.println("***");



    }
}
